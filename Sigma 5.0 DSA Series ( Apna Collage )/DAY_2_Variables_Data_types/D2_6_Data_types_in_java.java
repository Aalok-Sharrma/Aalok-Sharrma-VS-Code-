package DAY_2_Variables_Data_types;

public class D2_6_Data_types_in_java {
    public static void main(String[] args) {
        
        // (i)  Primitive Datatype     :- byte,short,char,boolean,int,long,float,double.....
        // (ii) Non-Primitive Datatype :- String,Array,class,object,interface......

        // Now we are going to learn primitive datatype in this lecture 
        // We going to learn non-primitive datatype in lecture of OOPS on DAY_14 

        byte    a = 15;
        short   b = 15;
        char    c = 'A';
        boolean d = true;
        boolean e = false;
        int     f = 15;
        long    g = 15;
        float   h = 15f;
        double  i = 15;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);

    }
}
