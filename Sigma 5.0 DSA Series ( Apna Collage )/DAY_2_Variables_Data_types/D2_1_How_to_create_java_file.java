 /*       Creating a Java file involves two main aspects: creating the file on your system and writing Java code within it.

1. Creating a .java file on your system:
   Using a text editor (e.g., Notepad, VS Code, Sublime Text):
   Open your preferred text editor.
   Write your Java code.
   Save the file with a .java extension. The filename should match the public class name within the file (e.g., if your class is named MyClass, save it as MyClass.java).
   Using an Integrated Development Environment (IDE) like Eclipse, IntelliJ IDEA, or NetBeans:
   Open your IDE.
   Create a new Java Project (if you don't have one already).
   Right-click on the source folder (or the package where you want to create the file) and select "New" -> "Class" (or "Java Class").
   Enter the class name and click "Finish." The IDE will automatically create the .java file with the basic class structure.
2. Writing Java code within the file:
   A basic Java file structure typically includes:
   Java

// Optional: package declaration
package com.example.myproject;

// Class definition
public class MyClass {

    // Main method (entry point for execution)
    public static void main(String[] args) {
        // Your code goes here
        System.out.println("Hello, Java!");
    }
}
Explanation:
package com.example.myproject;: An optional package declaration that organizes your classes.
public class MyClass { ... }: Defines a public class named MyClass. The filename must match this class name.
public static void main(String[] args) { ... }: The main method, which is the entry point for executing your Java program.
System.out.println("Hello, Java!");: A simple statement to print "Hello, Java!" to the console.
After creating and saving your .java file, you can compile and run it using the Java Development Kit (JDK).  */

