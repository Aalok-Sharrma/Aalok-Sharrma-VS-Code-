package DAY_5_Loops_Flow_Control;

public class D5_3_Print_number_1_to_10 {
    public static void main(String[] args) {
        
        int counter=1;
        while (counter<=10){
            System.out.println(counter);
            counter++;
        }System.out.println("We have printed numbers from 1 to 10");

    }
}
